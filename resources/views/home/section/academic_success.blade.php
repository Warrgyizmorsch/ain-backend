<script>
document.addEventListener('DOMContentLoaded', () => {
  const mql = window.matchMedia('(max-width: 500px)');
  const wraps = document.querySelectorAll('.readmore');

  wraps.forEach((wrap, idx) => {
    const content = wrap.querySelector('.readmore-content');
    const btn = wrap.querySelector('.readmore-toggle');
    if (!content || !btn) return;

    if (!content.id) content.id = `rm-${idx+1}`;
    btn.setAttribute('aria-controls', content.id);

    const lines = wrap.dataset.lines || 8;
    wrap.style.setProperty('--rm-lines', lines);

    const applyViewportState = () => {
      if (mql.matches) {
        // Mobile: collapsed by default
        wrap.classList.remove('expanded');
        btn.style.display = '';
        btn.textContent = 'Read more';
        btn.setAttribute('aria-expanded', 'false');
      } else {
        // Desktop: always expanded
        wrap.classList.add('expanded');
        btn.style.display = 'none';
        btn.setAttribute('aria-expanded', 'true');
      }
    };

    applyViewportState();
    mql.addEventListener('change', applyViewportState);

    btn.addEventListener('click', () => {
      const expanded = wrap.classList.toggle('expanded');
      btn.textContent = expanded ? 'Read less' : 'Read more';
      btn.setAttribute('aria-expanded', expanded ? 'true' : 'false');
    });
  });
});
</script>
