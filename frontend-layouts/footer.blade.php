<script>
  document.addEventListener("DOMContentLoaded", () => {
    const menuItems = document.querySelectorAll(".list .has-submenu");

    // Existing dropdown behavior for large screens
    menuItems.forEach((item) => {
      item.addEventListener("click", () => {
        document.querySelectorAll(".list .submenu").forEach((submenu) => {
          if (submenu !== item.querySelector(".submenu")) submenu.style.display = "none";
        });

        const submenu = item.querySelector(".submenu");
        if (submenu) {
          const isVisible = submenu.style.display === "block";
          submenu.style.display = isVisible ? "none" : "block";
        }
      });
    });

    // ✅ Mobile accordion only for footer headings
    const footerHeads = document.querySelectorAll(".footer_column_Links, .contact-us-box");

    footerHeads.forEach((head) => {
      head.addEventListener("click", () => {
        if (window.innerWidth > 768) return;

        const list = head.nextElementSibling;
        if (!list || !list.classList || !list.classList.contains("list")) return;

        // Collapse other lists
        document.querySelectorAll(".widgets-section .list").forEach((otherList) => {
          if (otherList !== list) otherList.style.display = "none";
        });

        // Toggle current list
        list.style.display = list.style.display === "block" ? "none" : "block";
      });
    });
  });

  // ✅ Ensure iconify scans entire page (content + footer)
  window.addEventListener("load", function () {
    if (window.Iconify && window.Iconify.scan) {
      window.Iconify.scan(document.body);
    }
  });
</script>
